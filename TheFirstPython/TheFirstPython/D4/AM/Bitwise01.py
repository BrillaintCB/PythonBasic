""" 
Python Bitwise Operators


Operators are used to perform operations on values and variables.
These are the special symbols 
that carry out arithmetic and 
logical ccomputations

    Bitwise operators

In Python, bitwise operators are used to perform bitwise calculations
on integers. The integers are first converted into 'binary' and then 
operations are performed on bit by bit,
hence the name bitwise operators. Then the result is returned in decimal format

"""

a = 10       # 1010 (Binary)
b = 4        # 0100 (Binary)

print("a & b= ", a & b)       
print("a | b= ", a | b)
print("~ a == ", ~a)
print("a ^ b =", a ^ b)

